# # Intuition
# Given an integer array nums, find a subarray that has the largest product, and return the product.

# # Approach
# 解法: (1)Kadane Algorithm (2)DP <最左/最右/中間 遞迴>
# 陷阱: 「最大乘積子陣列」需同時追蹤 最大值 和 最小值，因為負數可能會翻轉結果

# # Complexity
# - Time complexity:O(n)
# - Space complexity:O(1)

